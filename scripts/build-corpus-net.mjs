// Offline trainer for the (to)complete online neural model.
//
// Pre-trains the fixed-window neural language model in workers/seb-feed/src/body-for-visits/net.js
// on the four pinned public-domain novels, then emits net-weights.generated.js —
// the warm-start weights the Durable Object loads on first run and then keeps
// fine-tuning online. The piece ships fluent; it never starts from noise.
//
// Run: npm run build:corpus-net
// Texts are cached under scripts/.cache/corpus-text so re-runs are offline.

import { writeFile, readFile, mkdir } from "node:fs/promises";
import { existsSync } from "node:fs";
import { pathToFileURL } from "node:url";

import {
  SOURCES,
  loadText,
  stripGutenberg,
  tokenizeText,
  collectProperTerms,
  BLOCKED_TERMS,
} from "./build-corpus-speech-model.mjs";
import {
  createNet,
  trainStep,
  forward,
  softmax,
  weightCount,
  snapshotWeights,
  floatsToBase64,
} from "../workers/seb-feed/src/body-for-visits/net.js";
import { hashTokens } from "../workers/seb-feed/src/body-for-visits/grammar.js";

const CACHE_DIR = new URL("./.cache/corpus-text/", import.meta.url);
const OUTPUT_PATH = new URL(
  "../workers/seb-feed/src/body-for-visits/net-weights.generated.js",
  import.meta.url
);

const CONFIG = Object.freeze({ V: 1200, K: 4, D: 40, H: 112 });
const TOKENS_PER_SOURCE = Number(process.env.CORPUS_NET_TOKENS || 40000);
const EPOCHS = Number(process.env.CORPUS_NET_EPOCHS || 6);
const BATCH = 64;
const LR0 = 0.05;
const LR_DECAY = 0.8;
const L2 = 1e-5;
const CLIP = 1.0;
const INIT_SEED = 0x5eed1234;

const BOS = "<bos>";
const UNK = "<unk>";
const PUNCT = [",", ";", "—"];

async function loadCached(source) {
  await mkdir(CACHE_DIR, { recursive: true });
  const cacheFile = new URL(`${source.id}.txt`, CACHE_DIR);
  if (existsSync(cacheFile)) {
    return readFile(cacheFile, "utf8");
  }
  const text = await loadText(source);
  await writeFile(cacheFile, text);
  return text;
}

function buildVocab(tokenStream) {
  const counts = new Map();
  for (const token of tokenStream) {
    if (token === "." || PUNCT.includes(token)) continue;
    counts.set(token, (counts.get(token) || 0) + 1);
  }
  const ranked = [...counts.entries()]
    .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))
    .map(([word]) => word);
  const vocab = [BOS, UNK, ...PUNCT];
  for (const word of ranked) {
    if (vocab.length >= CONFIG.V) break;
    vocab.push(word);
  }
  while (vocab.length < CONFIG.V) vocab.push(`<pad${vocab.length}>`);
  return vocab;
}

// Slices the token stream into sentences (split on "."), maps to ids, and
// emits (context[K], target) pairs with <bos> left-padding. UNK targets are
// skipped so the model is never trained to predict the unknown bucket.
function buildExamples(tokenStream, word2id) {
  const { K } = CONFIG;
  const bos = word2id.get(BOS);
  const unk = word2id.get(UNK);
  const ctx = [];
  const targets = [];
  let sentence = [];

  const flush = () => {
    const wordCount = sentence.filter((id) => id !== unk).length;
    if (wordCount >= 2) {
      const padded = new Array(K).fill(bos).concat(sentence);
      for (let t = 0; t < sentence.length; t++) {
        const target = sentence[t];
        if (target === unk) continue;
        for (let k = 0; k < K; k++) ctx.push(padded[t + k]);
        targets.push(target);
      }
    }
    sentence = [];
  };

  for (const token of tokenStream) {
    if (token === ".") {
      flush();
      continue;
    }
    const id = word2id.get(token);
    sentence.push(id == null ? unk : id);
  }
  flush();

  return { ctx: Int32Array.from(ctx), targets: Int32Array.from(targets) };
}

function shuffledIndices(n, rng) {
  const idx = new Int32Array(n);
  for (let i = 0; i < n; i++) idx[i] = i;
  for (let i = n - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    const tmp = idx[i];
    idx[i] = idx[j];
    idx[j] = tmp;
  }
  return idx;
}

function mulberry32(seed) {
  let a = seed >>> 0;
  return function () {
    a = (a + 0x6d2b79f5) >>> 0;
    let t = a;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function evalLoss(net, ctx, targets, K, sampleSize) {
  const n = targets.length;
  const stride = Math.max(1, Math.floor(n / sampleSize));
  let loss = 0;
  let count = 0;
  for (let i = 0; i < n; i += stride) {
    const ids = ctx.subarray(i * K, i * K + K);
    const { logits } = forward(net, ids);
    const p = softmax(logits);
    loss += -Math.log(Math.max(p[targets[i]], 1e-9));
    count++;
  }
  return count > 0 ? loss / count : 0;
}

function renderFile(model) {
  return `// Generated by scripts/build-corpus-net.mjs — do not edit by hand.
// Warm-start weights for the (to)complete online neural language model,
// pre-trained on the four pinned public-domain Project Gutenberg novels.
// The Durable Object loads these on first run, then fine-tunes online.

export const NET_MODEL = Object.freeze({
  version: ${model.version},
  config: Object.freeze(${JSON.stringify(model.config)}),
  vocabHash: ${JSON.stringify(model.vocabHash)},
  sources: Object.freeze(${JSON.stringify(model.sources)}),
  vocab: Object.freeze(${JSON.stringify(model.vocab)}),
  blockedTerms: Object.freeze(${JSON.stringify(model.blockedTerms)}),
  weightsB64: ${JSON.stringify(model.weightsB64)},
});
`;
}

async function main() {
  console.log(`[corpus-net] config ${JSON.stringify(CONFIG)} — ${weightCount(CONFIG)} params`);

  const stripped = [];
  for (const source of SOURCES) {
    const raw = await loadCached(source);
    stripped.push(stripGutenberg(raw));
    console.log(`[corpus-net] loaded ${source.id}`);
  }

  const blocked = new Set([...BLOCKED_TERMS, ...collectProperTerms(stripped)]);
  const tokenStream = [];
  for (const text of stripped) {
    const tokens = tokenizeText(text, blocked);
    for (let i = 0; i < tokens.length && i < TOKENS_PER_SOURCE; i++) {
      tokenStream.push(tokens[i]);
    }
  }
  console.log(`[corpus-net] ${tokenStream.length} training tokens`);

  const vocab = buildVocab(tokenStream);
  const word2id = new Map(vocab.map((w, i) => [w, i]));
  const vocabHash = hashTokens(vocab).toString(16);

  const { ctx, targets } = buildExamples(tokenStream, word2id);
  const n = targets.length;
  console.log(`[corpus-net] ${n} training examples, vocab ${vocab.length}`);

  const net = createNet(CONFIG, INIT_SEED);
  const rng = mulberry32(0x13579bdf);
  const { K } = CONFIG;

  for (let epoch = 0; epoch < EPOCHS; epoch++) {
    const lr = LR0 * Math.pow(LR_DECAY, epoch);
    const order = shuffledIndices(n, rng);
    let epochLoss = 0;
    let steps = 0;
    for (let start = 0; start < n; start += BATCH) {
      const batch = [];
      for (let b = start; b < Math.min(n, start + BATCH); b++) {
        const i = order[b];
        const c = new Array(K);
        for (let k = 0; k < K; k++) c[k] = ctx[i * K + k];
        batch.push({ ctx: c, target: targets[i] });
      }
      epochLoss += trainStep(net, batch, { lr, clip: CLIP, decay: L2 });
      steps++;
    }
    const evalL = evalLoss(net, ctx, targets, K, 4000);
    console.log(
      `[corpus-net] epoch ${epoch + 1}/${EPOCHS} lr=${lr.toFixed(4)} ` +
        `trainLoss=${(epochLoss / steps).toFixed(4)} evalLoss=${evalL.toFixed(4)}`
    );
  }

  const weights = snapshotWeights(net);
  const model = {
    version: 1,
    config: CONFIG,
    vocabHash,
    sources: SOURCES.map(({ id, title, author, url }) => ({ id, title, author, url })),
    vocab,
    blockedTerms: [...blocked].sort(),
    weightsB64: floatsToBase64(weights),
  };
  await writeFile(OUTPUT_PATH, renderFile(model));
  console.log(
    `[corpus-net] wrote ${OUTPUT_PATH.pathname} ` +
      `(${weights.length} floats, ${(model.weightsB64.length / 1024).toFixed(0)} KiB base64)`
  );
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
